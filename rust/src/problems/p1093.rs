pub struct Solution;

impl Solution {
    pub fn sample_stats(count: Vec<i32>) -> Vec<f64> {
        let mut min = 255;
        let mut max = 0;
        let n = count.iter().sum::<i32>();
        let mut sum = 0;
        let (mut mode, mut f) = (0, 0);
        let left = (n + 1) / 2;
        let right = (n + 2) / 2;
        let mut median = 0;
        let mut cnt = 0;
        for (i, &c) in count.iter().enumerate() {
            if c == 0 {
                continue;
            }
            if c > f {
                mode = i;
                f = c;
            }
            sum += i as i64 * c as i64;
            min = i.min(min);
            max = i.max(max);
            if cnt < left && c + cnt >= left {
                median += i;
            }
            if cnt < right && c + cnt >= right {
                median += i;
            }
            cnt += c;
        }
        vec![
            min as f64,
            max as f64,
            sum as f64 / n as f64,
            median as f64 / 2.0,
            mode as f64,
        ]
    }
}

